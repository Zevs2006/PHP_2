<?php
// Массив с числами
$numbers = [3, 7, 1, 9, 5];

// Переменная для накопления суммы
$sum = 0;

// Проходим по каждому элементу массива и добавляем его к сумме
foreach ($numbers as $number) {
    $sum += $number;
}

// Выводим итоговую сумму
echo "Сумма всех элементов массива: $sum";

